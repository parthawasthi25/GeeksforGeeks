class Solution {
  public:
    int SumofMiddleElements(vector<int> &arr1, vector<int> &arr2){
        vector<int> arr3;
        int sum=0;
        arr3.insert(arr3.end(),arr1.begin(),arr1.end());
        arr3.insert(arr3.end(),arr2.begin(),arr2.end());
        sort(arr3.begin(),arr3.end());
        int n3=arr3.size();
        sum = arr3[n3/2] + arr3[(n3/2)-1];
        return sum;
    }
};

